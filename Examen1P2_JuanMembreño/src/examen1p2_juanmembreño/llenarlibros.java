    private void libros1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_libros1ItemStateChanged

        for (int i = 0; i < listab.size(); i++) {
            if (listab.get(i).getGenero().equalsIgnoreCase("fantasia")) {
                listaf.add(listab.get(i));
                mostrarlibros.setText(listaf.toString());
            }
            if (listab.get(i).getGenero().equalsIgnoreCase("historia")) {
                listah.add(listab.get(i));
                mostrarlibros.setText(listah.toString());
            }
            if (listab.get(i).getGenero().equalsIgnoreCase("romance")) {
                listar.add(listab.get(i));
                mostrarlibros.setText(listar.toString());
            }
            if (listab.get(i).getGenero().equalsIgnoreCase("Accion")) {
                listaa.add(listab.get(i));
                mostrarlibros.setText(listaa.toString());
            }
        }

    }//GEN-LAST:event_libros1ItemStateChanged

