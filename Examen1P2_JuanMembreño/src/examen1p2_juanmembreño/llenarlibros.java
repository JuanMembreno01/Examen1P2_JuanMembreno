    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String titulo, descripcion, autor, genero;
        genero = "";
        int puntaje, valor, edicion, copiasdis;
        Date anop;
        try {
            titulo = titulo1.getText();
            descripcion = descrip1.getText();
            autor = autor1.getText();
            if (gr.isSelected()) {
                genero = "romance";
            }
            if (gf.isSelected()) {
                genero = "fantasia";
            }
            if (ga.isSelected()) {
                genero = "Accion";
            }
            if (gh.isSelected()) {
                genero = "Historia";
            }
            // codigo = Integer.parseInt(tf_codigo.getText());
            puntaje = Integer.parseInt(puntaje1.getText());
            valor = Integer.parseInt(valor1.getText());
            edicion = Integer.parseInt(edicion1.getText());
            copiasdis = Integer.parseInt(copias1.getText());
            anop = añodp1.getDate();
            libros m = new libros(titulo, descripcion, puntaje, copiasdis, genero, valor, edicion, autor, anop);
            listab.add(m);
            DefaultComboBoxModel jc = (DefaultComboBoxModel) libros1.getModel();
            jc.addElement(m);
            libros1.setModel(jc);
            JOptionPane.showMessageDialog(null, "Agregado exitosamente");
            titulo1.setText("");
            descrip1.setText("");
            autor1.setText("");
            ga.setSelected(false);
            gr.setSelected(false);
            gh.setSelected(false);
            gf.setSelected(false);
            puntaje1.setText("");
            valor1.setText("");
            edicion1.setText("");
            copias1.setText("");
            añodp1.setDate(new Date());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error y no se guardaron los datos");
        }


    }//GEN-LAST:event_jButton2MouseClicked

